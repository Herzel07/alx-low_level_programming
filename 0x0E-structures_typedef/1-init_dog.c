#include "dog.h"
#include <stdlib.h>

/**
 * init_dog - initialize a variable of type struct dog
 * @d: dog to be initialized
 * @name: name of the dog
 * @age: the age of the dog
 * @owner: the dog's owner
 */

void init_dog(struct dog *d, char *name, float age, char *owner)
{
	if (d != NULL)
	{
		d->name;
		d->age;
		d->owner;
	}
}
